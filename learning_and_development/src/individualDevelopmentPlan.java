    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        
        
        /*
            print form
        */
        PrinterJob pJob = PrinterJob.getPrinterJob();
        pJob.setJobName("Print form");

        PageFormat pf = pJob.defaultPage();
        pf.setPaper(new Paper());
        pf.getPaper().setImageableArea(0, 0, 612, 1008); // Legal size paper
        pf.getPaper().setSize(612, 1008); // Legal size paper

        pJob.setPrintable(new Printable(){
            @Override
            public int print(Graphics pg, PageFormat pf, int pageNum){

                if(pageNum > 0){
                    return Printable.NO_SUCH_PAGE;
                }

                Graphics2D g2 = (Graphics2D)pg;
                g2.translate((int) pf.getImageableX(), (int) pf.getImageableY());

                // Calculate the scale factor to fit the panel to the paper
                double scaleFactor = Math.min(pf.getImageableWidth() / jPanel2.getWidth(), pf.getImageableHeight() / jPanel2.getHeight());

                // Apply the scale factor
                g2.scale(scaleFactor, scaleFactor);

                jPanel2.printAll(g2);
                return Printable.PAGE_EXISTS;
            }
        });

        boolean ok = pJob.printDialog();
        if(ok)
        {
            try{
                pJob.print();
            } catch (PrinterException ex) {
                Logger.getLogger(individualDevelopmentPlan.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

