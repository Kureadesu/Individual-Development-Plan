    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        
        // Collect input data from all JPanel components
        List<String> data = new ArrayList<>();

        for (int i = 0; i < jPanel2.getComponentCount(); i++) {
            Component component = jPanel2.getComponent(i);
            if (component instanceof JTextField) {
                JTextField textField = (JTextField) component;
                data.add(textField.getText());
            } else if (component instanceof JTextArea) {
                JTextArea textArea = (JTextArea) component;
                data.add(textArea.getText());
            } else if (component instanceof JComboBox) {
                JComboBox comboBox = (JComboBox) component;
                data.add((String) comboBox.getSelectedItem());
            } else if (component instanceof JTable) {
                JTable table = (JTable) component;
                TableModel tableModel = table.getModel();
                int rowCount = tableModel.getRowCount();
                for (int j = 0; j < rowCount; j++) {
                    StringBuilder rowData = new StringBuilder();
                    for (int k = 0; k < tableModel.getColumnCount(); k++) {
                        rowData.append(tableModel.getValueAt(j, k)).append(",");
                    }
                    data.add(rowData.toString());
                }
            }
        }

        // Write data to CSV file
        String csvFile = "export.csv";
        try (BufferedWriter writer = new BufferedWriter(new FileWriter(csvFile))) {
            for (String datum : data) {
                writer.write(datum + ",");
            }
            writer.newLine();
        } catch (IOException e) {
            System.err.println("Error writing to CSV file: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

